# module 모듈 : 파이썬은 모듈을 활용해 코드를 분리하고 공유
# 모듈은 여러 변수와 함수를 까지고 있는 집합체로 표준 모듈과 외부 모듈로 구분.
# 표준 모듈: 파이썬에 기본적으로 내장되어 있는 모듈
# 외부 모듈: 다른 사람들이 만들어서 꽁개한 모듈
# import 모듈 이름

# 모듈 사용의 기본: math 모듈
import math
print(math.sin(1))
print(math.cos(1))
print(math.tan(1))
print(math.floor(2.5)) # 내림
print(math.ceil(2.5)) # 올림

